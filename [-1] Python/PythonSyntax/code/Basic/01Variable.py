# 变量
# 变量概念
x = 1
# Python中严格区分大小写
# 首字符不允许为数字
# 变量名之间不可以有空格
# 不可与保留字相同

# 变量名命名技巧
# 变量名近可能有实际意义，表征数据的某种特性
age_of_students = [17, 18, 19]
# 下划线 变量名由多个单词组成：用_连接
# 驼峰体：变量名由多个单词组成，单词首字母大写
AgeOfStudents = [17, 18, 19]
# 尽量避免用中文和拼音做变量名
# 常量的所有字母都大写
MAX_ITERATION = 1000

# 变量的赋值
# 一般赋值
x = 1 + 2
print(x)
# 增量赋值
x = 10
x = x + 1
print(x)
x -= 1
print(x)
# 打包赋值
x, y = 1, 2
print(x, y)
x, y = y, x # 交换
print(x, y)


